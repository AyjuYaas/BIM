void add(float x, float y){
	float sum = x + y;
	printf ("\nSum = %f", sum);	
}

void sub(float x, float y){
	float sub = x - y;
	printf ("\nDifference = %f", sub);	
}

void mul(float x, float y){
	float mul = x * y;
	printf ("\nMultiplication =  %f", mul);	
}

void div(float x, float y){
	float div = x / y;
	printf ("\nDivision = %f", div);	
}
